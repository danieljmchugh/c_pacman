int input()
{
    char input;
    
    scanf(" %c", &input); // space before to ignore whitespaces 

    switch (input) {
        case 'd':
            return 1;
            break;
        case 'a':
            return 2;
            break;
        case 'w':
            return 3;
            break;
        case 's':
            return 4;
            break;
        default:
            return 0;
            break;
    }
}


void print_matrix(char map[MAX_ROWS][MAX_COLS])
{
    for (int row = 0; row < MAX_ROWS; row++) {
        printf(": ");
        for (int col = 0; col < MAX_COLS; col++) {
            
            printf("%c ", map[row][col]);
        }
        printf("\n");
    }
}
